package com.basic.bai1;

public class CalculateUtils {
    static double tinhTong(double x, double y) {
        double Tong = x + y;
        return Tong;
    }
    static double tinhNhan(double x, double y) {
        double Nhan = x * y;
        return Nhan;
    }
    static double tinhHieu(double x, double y) {
        double Hieu = x - y;
        return Hieu;
    }
    static double tinhChia(double x, double y) {
        double Chia = x/y;
        return Chia;
    }
}

